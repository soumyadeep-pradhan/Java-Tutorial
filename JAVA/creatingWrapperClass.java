public class creatingWrapperClass {
    public static void main(String[] args) {
        Integer myInteger = 54;
        Boolean myBoolean = true;
        Double myDouble = 8.54;
        Character myCharacter = 'R';
        System.out.println(myBoolean.booleanValue());
        System.out.println(myInteger.intValue());
        System.out.println(myDouble.doubleValue());
        System.out.println(myCharacter.charValue());
    }
}
